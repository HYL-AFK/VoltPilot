static bool find_glyph(const glyph_entry_t *entries, size_t count, char character,
                       vp_ui_glyph_t *out_glyph)
{
    if (entries == NULL || out_glyph == NULL) {
        return false;
    }
    for (size_t i = 0; i < count; ++i) {
        if (entries[i].character == character) {
            *out_glyph = entries[i].glyph;
            return true;
        }
    }
    return false;
}

bool vp_ui_font_get_glyph(vp_ui_font_id_t font, char character, vp_ui_glyph_t *out_glyph)
{
#define FIND(table) find_glyph((table), sizeof(table) / sizeof((table)[0]), character, out_glyph)
    switch (font) {
    case VP_UI_FONT_METRIC: return FIND(s_metric_glyphs);
    case VP_UI_FONT_SOC: return FIND(s_soc_glyphs);
    case VP_UI_FONT_PERCENT: return FIND(s_percent_glyphs);
    case VP_UI_FONT_GEAR: return FIND(s_gear_glyphs);
    case VP_UI_FONT_STATE: return FIND(s_state_glyphs);
    default: return false;
    }
#undef FIND
}

int vp_ui_font_measure(vp_ui_font_id_t font, const char *text)
{
    int width = 0;
    vp_ui_glyph_t glyph;
    for (const char *p = text; p != NULL && *p != '\0'; ++p) {
        if (vp_ui_font_get_glyph(font, *p, &glyph)) {
            width += glyph.advance;
        }
    }
    return width;
}
